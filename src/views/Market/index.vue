<template>
  <div class="market-layout">
    <el-card shadow="always" class="market-head flex">
      <el-button type="primary" @click="registerVisible = true">注册应用</el-button>
      <!-- <el-button type="primary" @click.stop="linkOrder()">订单</el-button> -->
      <el-button type="primary" @click="GoPage('/market/markList')">去市场</el-button>
    </el-card>
    <div class="market-content box">
      <ul class="box-ul">
        <p class="box-ul-title">我的应用</p>
        <li class="app-card">
          <MarketCreate :info="add" @myclick="GoPage('/market/markList')" />
          <ShopCard
            v-for="item in state.ownProductList"
            :info="item"
            :key="item.id"
            :over-id="item.id"
          >
            <!-- <template> -->
            <el-dropdown @command="(value) => handleCommand('own', value, item)" placement="top">
              <el-button class="btn" type="primary" link small> 设置 </el-button>
              <template #dropdown>
                <el-dropdown-menu>
                  <el-dropdown-item v-for="action in actionOptionsOfOwn" :command="action.value">
                    {{ action.label }}
                  </el-dropdown-item>
                </el-dropdown-menu>
              </template>
            </el-dropdown>
            <el-divider direction="vertical" />
            <el-button class="btn" link small @click="deleteApp(item)">移除应用</el-button>
          </ShopCard>
        </li>
        <el-pagination
          style="justify-content: end"
          hide-on-single-page
          layout="prev, pager, next"
          :total="state.ownTotal"
        />
      </ul>
      <ul class="box-ul">
        <p class="box-ul-title">其他应用</p>
        <li class="app-card">
          <ShopCard v-for="item in state.shareProductList" :info="item" :key="item.id" :over-id="item.id">
            <el-dropdown @command="(value) => handleCommand('other', value, item)" placement="top">
              <el-button class="btn" type="primary" link small> 设置 </el-button>
              <template #dropdown>
                <el-dropdown-menu>
                  <el-dropdown-item v-for="action in actionOptionsOfOther" :command="action.value">
                    {{ action.label }}
                  </el-dropdown-item>
                </el-dropdown-menu>
              </template>
            </el-dropdown>
            <el-divider direction="vertical" />
            <el-button class="btn" link small @click="deleteApp(item)">移除应用</el-button>
          </ShopCard>
        </li>
        <el-pagination
          style="justify-content: end"
          hide-on-single-page
          layout="prev, pager, next"
          :total="state.shareTotal"
        />
      </ul>
    </div>
  </div>
  <el-dialog
    v-model="registerVisible"
    title="注册应用"
    width="600px"
    draggable
    :close-on-click-modal="false"
  >
    <el-form ref="registerFormRef" :model="form" label-width="120px" :rules="rules">
      <el-form-item label="应用名称" prop="name">
        <el-input v-model="form.name" />
      </el-form-item>
      <el-form-item label="应用编码" prop="code">
        <el-input v-model="form.code" />
      </el-form-item>
      <el-form-item label="备注" prop="remark">
        <el-input v-model="form.remark" :rows="3" type="textarea" placeholder="请输入描述/备注" />
      </el-form-item>
      <!-- <el-form-item label="应用thingId" prop="thingId">
        <el-input v-model="form.thingId" />
      </el-form-item> -->
    </el-form>
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="resetForm(registerFormRef)">取消</el-button>
        <el-button type="primary" @click="onRegisterSubmit(registerFormRef)">确认</el-button>
      </span>
    </template>
  </el-dialog>
  <el-dialog
    v-model="publishVisible"
    title="上架应用"
    width="600px"
    draggable
    :close-on-click-modal="false"
  >
    <putaway-comp :info="selectProductItem" ref="putawayRef" @closeDialog="publishVisible = false">
      <template #btns>
        <div class="putaway-footer" style="text-align: right">
          <el-button @click="publishVisible = false">取消</el-button>
          <el-button type="primary" @click="putawaySubmit()"> 确认</el-button>
        </div>
      </template>
    </putaway-comp>
  </el-dialog>
  <el-dialog
    v-model="shareVisible"
    custom-class="share-dialog"
    title="分享应用"
    width="600px"
    draggable
    :close-on-click-modal="false"
  >
    <share-comp :info="selectProductItem" />
  </el-dialog>
</template>

<script setup lang="ts">
  import API from '@/services'
  import { ElMessage, ElMessageBox } from 'element-plus'
  import { onMounted, reactive, ref } from 'vue'
  import ShopCard from './components/shopCard.vue'
  import PutawayComp from './components/putawayComp.vue'
  import ShareComp from './components/shareComp.vue'
  import { baseData, actionOptionsOfOther, actionOptionsOfOwn } from './config'
  import { useRouter } from 'vue-router'
  import type { FormInstance, FormRules } from 'element-plus'
  import MarketCreate from './components/marketCreate.vue'
  const add: string = '从应用市场中添加'
  // 注册页面实例
  const registerFormRef = ref<FormInstance>()
  const router = useRouter()
  type StateType = {
    ownProductList: ProductType[] //我的应用
    ownTotal: number
    shareProductList: ProductType[] //其他应用
    shareTotal: number
    marketOptions: any[] //所有市场列表
  }

  const state: StateType = reactive({
    ownProductList: [],
    ownTotal: 0,
    shareProductList: [],
    shareTotal: 0,
    marketOptions: []
  })

  onMounted(() => {
    // 获取列表
    getProductList('own')
    getProductList('share')
  })

  // 获取我的应用列表
  const getProductList = async (type: 'own' | 'share') => {
    const { data, success } = await API.product[
      type === 'own' ? 'searchOwnProduct' : 'searchShareProduct'
    ]({
      data: { offset: 0, limit: 10, filter: '' }
    })
    if (success) {
      const { result = [], total = 0 } = data
      state[`${type}ProductList`] = [...result]
      state[`${type}Total`] = total
    }
  }

  // 移除app
  const deleteApp = (item: any) => {
    ElMessageBox.confirm(`确认删除  ${item.name}?`, '警告', {
      confirmButtonText: '确认',
      cancelButtonText: '取消',
      type: 'warning'
    })
      .then(async () => {
        const { success } = await API.product.delete({
          data: { id: item.id }
        })
        if (success) {
          getProductList('own')
          ElMessage({
            type: 'success',
            message: '操作成功'
          })
        }
      })
      .catch(() => {})
  }

  // 记录当前操作的 应用信息
  const selectProductItem = ref<ProductType>()
  // 处理 设置 菜单选择事件
  const handleCommand = (
    type: 'own' | 'other',
    command: string | number | object,
    item: ProductType
  ) => {
    console.log('菜单选择事件', type, command, item)
    selectProductItem.value = item
    switch (command) {
      case 'share':
        shareVisible.value = true
        break
      case 'putaway':
        publishVisible.value = true
        break
      case 'unsubscribe':
        break
      default:
        break
    }
  }

  // 注册页面弹窗
  const registerVisible = ref<boolean>(false)

  // 注册信息
  const form = reactive({
    name: '',
    code: '',
    remark: ''
  })

  // 提交注册
  const onRegisterSubmit = (formEl: FormInstance | undefined) => {
    if (!formEl) return
    formEl.validate(async (valid, fields) => {
      if (valid) {
        console.log('submit!', form)
        const { success } = await API.product.register({
          data: form
        })
        if (success) {
          getProductList('own')
          ElMessage({
            type: 'success',
            message: '应用注册成功'
          })
          resetForm(formEl)
        }
      } else {
        console.log('error submit!', fields)
      }
    })
  }
  // 注册验证规则
  const rules = reactive<FormRules>({
    name: [
      { required: true, message: '请输入应用名称', trigger: 'blur' },
      { min: 2, max: 8, message: '长度限制2-8', trigger: 'blur' }
    ],
    code: [
      {
        required: true,
        message: '请输入应用编码',
        trigger: 'blur'
      }
    ]
  })

  // 重置注册表单
  const resetForm = (formEl: FormInstance) => {
    registerVisible.value = false
    if (!formEl) return
    formEl.resetFields()
  }

  // 上架应用功能
  const publishVisible = ref<boolean>(false)
  const putawayRef = ref<any>()
  // 提交上架
  const putawaySubmit = () => {
    putawayRef.value.onPutawaySubmit()
  }

  // 分享功能
  const shareVisible = ref<boolean>(false)

  // 路由跳转
  const GoPage = (path: string) => {
    router.push(path)
  }
</script>

